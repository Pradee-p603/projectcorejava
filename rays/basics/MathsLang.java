package in.co.rays.basics;

public class MathsLang {
public static void main(String[] args) {
	System.out.println("mathematics operation");
	System.out.println("Max 3,4 ="   +Math.max(3,4));
	System.out.println("min 100 , 99 = "  +Math.min(100, 99));
	System.out.println("Absolute 4.7= "  +Math.abs(4.7));
	System.out.println("Square Root="     +Math.sqrt(16));
	System.out.println("Exp 10="          +Math.exp(10));
	System.out.println("Random number="   +Math.random()*1000);
}
}
